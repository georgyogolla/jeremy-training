// VARIABLES
// Variables are like "labeled jars"
// for a value in JavaScript.
// We can store a value and give it a
// name, so that we can...
// - recall it
// - use it
// - or change it later on.

// Declaring variables(syntax)
// let someName = value 

// Recall variables
let numOne = 3;
let numTwo = 4;
console.log(numOne + numTwo);

// Updating variables
console.log(numOne - 1);//2
console.log(numOne); //3
console.log(numOne = numOne - 1);//2
console.log(numOne);// 2

